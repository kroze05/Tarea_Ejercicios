# EJERCICIO 2.- Calcular el perímetro y área de un círculo dado su radio.
print("calculo del perímetro y area de un circulo con su radio")
val=int(input("coloque el radio del circulo(en metros): "))
perimetro = val * 2 * 3.1416
area = 3.1416 * (val **2)
print(f"el perimetro es igual a:{perimetro} \ny el area es igual a:{area}")

